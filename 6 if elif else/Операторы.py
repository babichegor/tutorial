#Операторы if(если) elif(иначе если) else(иначе)
age = 29
if age == 29:
    print('Одногодки')
elif age <= 29:
    print('Моложе меня')
elif age >= 29:
    print('Старше меня')
else:
    print('Ну тут я уче хз')

