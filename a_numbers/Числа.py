#Сложение чисел
print(5 + 9)

#Вычитание
print(100 - 7)

#Умножение
print(6 * 7)

#Деление с плавающей точкой
print(9 / 5)

#Целочисленное деление дает ответ отбрасывая остаток
print(9 // 5)

#Остаток от деления
print(9 % 5)

#Частное и остаток одновременно divmod() это функция
print(divmod(9, 5))

#Преобразования типов
int() #Целое число
float() #Число с плавающей точкой